enum class Destination(
    val route: String,
    val icon: ImageVector,
    val stringRes: Int
) {
    SONGS("songs",  Icons.Default.ShoppingCart,  com.example.myapplication.R.string.first),
    ALBUM("album",   Icons.Default.Favorite , com.example.myapplication.R.string.second),
    PLAYLISTS("playlist",  Icons.Default.AccountBox, com.example.myapplication.R.string.third)
}

@Composable
fun AppNavHost(
    navController: NavHostController,
    startDestination: Destination,
    modifier: Modifier = Modifier
) {
    NavHost(
        navController,
        startDestination = startDestination.route
    ) {
        Destination.entries.forEach { destination ->
            composable(destination.route) {
                when (destination) {
                    Destination.SONGS -> Shop()
                    Destination.ALBUM -> Favorite()
                    Destination.PLAYLISTS -> ProfileLayout()
                }
            }
        }
    }
}

@Preview
@Composable
fun NavigationBarExample(modifier: Modifier = Modifier) {
    val navController = rememberNavController()
    val startDestination = Destination.SONGS
    var selectedDestination by rememberSaveable { mutableIntStateOf(startDestination.ordinal) }

    Scaffold(
        modifier = modifier,
        bottomBar = {
            NavigationBar(windowInsets = NavigationBarDefaults.windowInsets) {
                Destination.entries.forEachIndexed { index, destination ->
                    NavigationBarItem(
                        selected = selectedDestination == index,
                        onClick = {
                            navController.navigate(route = destination.route)
                            selectedDestination = index
                        },
                        icon = {
                            Icon(
                                destination.icon,
                                contentDescription = stringResource(destination.stringRes)
                            )
                        },
                        label = { Text(stringResource(destination.stringRes)) }
                    )
                }
            }
        }
    ) { contentPadding ->
        AppNavHost(navController, startDestination, modifier = Modifier.padding(contentPadding))
    }
}

